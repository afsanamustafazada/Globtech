function initSliders() {
  document.querySelectorAll("[data-slider]").forEach((slider) => {
    const slides = Array.from(slider.querySelectorAll(".slider-slide"));
    const dotsContainer = slider.querySelector(".slider-dots");
    const previousButton = slider.querySelector("[data-slide-prev]");
    const nextButton = slider.querySelector("[data-slide-next]");
    let activeIndex = Math.max(0, slides.findIndex((slide) => slide.classList.contains("is-active")));
    let autoplayTimer;

    if (!slides.length) {
      return;
    }

    function setActive(index) {
      activeIndex = (index + slides.length) % slides.length;
      slides.forEach((slide, slideIndex) => {
        slide.classList.toggle("is-active", slideIndex === activeIndex);
      });

      if (dotsContainer) {
        dotsContainer.querySelectorAll(".slider-dot").forEach((dot, dotIndex) => {
          dot.classList.toggle("is-active", dotIndex === activeIndex);
          dot.setAttribute("aria-pressed", String(dotIndex === activeIndex));
        });
      }
    }

    function startAutoplay() {
      window.clearInterval(autoplayTimer);

      if (slides.length < 2) {
        return;
      }

      autoplayTimer = window.setInterval(() => {
        setActive(activeIndex + 1);
      }, Number(slider.dataset.interval || 6000));
    }

    if (dotsContainer) {
      dotsContainer.innerHTML = "";
      slides.forEach((_, index) => {
        const dot = document.createElement("button");
        dot.type = "button";
        dot.className = "slider-dot";
        dot.setAttribute("aria-label", `Slide ${index + 1}`);
        dot.addEventListener("click", () => {
          setActive(index);
          startAutoplay();
        });
        dotsContainer.append(dot);
      });
    }

    previousButton?.addEventListener("click", () => {
      setActive(activeIndex - 1);
      startAutoplay();
    });
    nextButton?.addEventListener("click", () => {
      setActive(activeIndex + 1);
      startAutoplay();
    });
    setActive(activeIndex);
    startAutoplay();
  });
}

function initMotion() {
  const revealTargets = document.querySelectorAll(
    [
      ".section-heading",
      ".intro-grid p",
      ".service-card",
      ".solution-tile",
      ".process-list li",
      ".standards-panel span",
      ".partner-card",
      ".ai-card",
      ".ai-visual-card",
      ".contact-form",
      ".project-card",
      ".gallery-item",
      ".demo-panel",
      ".demo-preview",
      ".slider-shell",
      ".facts-card",
    ].join(", ")
  );

  if (!revealTargets.length || window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
    return;
  }

  document.body.classList.add("motion-ready");

  revealTargets.forEach((element) => {
    element.classList.add("reveal-on-scroll");
  });

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      rootMargin: "0px 0px -12% 0px",
      threshold: 0.12,
    }
  );

  revealTargets.forEach((element) => observer.observe(element));
  window.setTimeout(() => {
    revealTargets.forEach((element) => element.classList.add("is-visible"));
  }, 900);
}

function initHeroCounters() {
  const counters = document.querySelectorAll("[data-counter]");

  if (!counters.length) {
    return;
  }

  const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  function animateCounter(counter) {
    if (counter.dataset.counted === "true") {
      return;
    }

    counter.dataset.counted = "true";
    const target = Number(counter.dataset.countTo || 0);
    const suffix = counter.dataset.suffix || "";

    if (prefersReducedMotion || !target) {
      counter.textContent = `${target}${suffix}`;
      return;
    }

    const startedAt = performance.now();
    const duration = 1100;

    function tick(now) {
      const progress = Math.min((now - startedAt) / duration, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      counter.textContent = `${Math.round(target * eased)}${suffix}`;

      if (progress < 1) {
        window.requestAnimationFrame(tick);
      }
    }

    counter.textContent = `0${suffix}`;
    window.requestAnimationFrame(tick);
  }

  if (!("IntersectionObserver" in window)) {
    counters.forEach(animateCounter);
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          animateCounter(entry.target);
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.45 }
  );

  counters.forEach((counter) => observer.observe(counter));
}

